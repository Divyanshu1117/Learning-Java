public class J005_Escape {
    public static void main(String[] args) {
        System.out.println("Hello 'DVCoding'...");
        System.out.println("Hello \"DVCoding\"...");
        System.out.println("Hello \\DVCoding\\...");
        System.out.println("Hello \n DVCoding...");
        System.out.println("Hello \t DVCoding...");
        System.out.println("Hello\bDVCoding...");
    }
}